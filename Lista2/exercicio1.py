#  Construir um programa que leia três valores numéricos inteiros (representados pelas variáveis A, B e C) 
#  e apresente como resultado final o valor do quadrado da soma dos três valores lidos.

a = int(input("Informe o valor A: "))
b = int(input("\nInforme o valor B: "))
c = int(input("\nInforme o valor C: "))

print("\nResultado: ", (a + b + c)**2)
